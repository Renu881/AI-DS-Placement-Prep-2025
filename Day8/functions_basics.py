# Basic function
def greet():
    print("Hello! Welcome to Python Functions.")

# Function with parameters
def greet_user(name):
    print(f"Hello, {name}!")

# Function with return value
def add_numbers(a, b):
    return a + b

# Function calls
greet()
greet_user("Renuka")
result = add_numbers(5, 7)
print("Sum:", result)
